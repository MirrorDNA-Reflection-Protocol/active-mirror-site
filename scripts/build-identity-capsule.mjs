import { readFileSync, writeFileSync } from "node:fs";
import { createHash } from "node:crypto";

const args = new Set(process.argv.slice(2));
const root = new URL("../", import.meta.url);
const contractUrl = new URL("identity/active-mirror-identity.json", root);
const llmsUrl = new URL("public/llms.txt", root);
const workerUrl = new URL("worker/src/identity-capsule.js", root);
const receiptUrl = new URL("identity/source-check-receipt.json", root);

const contract = JSON.parse(readFileSync(contractUrl, "utf8"));

function assertString(value, label) {
  if (typeof value !== "string" || !value.trim()) throw new Error(`${label} must be a non-empty string`);
}

function assertStringArray(value, label) {
  if (!Array.isArray(value) || value.length === 0) throw new Error(`${label} must be a non-empty array`);
  value.forEach((item, index) => assertString(item, `${label}[${index}]`));
}

function validateContract(value) {
  assertString(value.schema_version, "schema_version");
  assertString(value.version, "version");
  for (const [key, url] of Object.entries(value.canonical_urls || {})) {
    assertString(url, `canonical_urls.${key}`);
    new URL(url);
  }
  assertStringArray(value.what_active_mirror_is, "what_active_mirror_is");
  assertStringArray(value.answer_rules, "answer_rules");
  assertStringArray(value.consumer_language?.prefer, "consumer_language.prefer");
  assertStringArray(value.consumer_language?.avoid_unless_asked, "consumer_language.avoid_unless_asked");
  assertStringArray(value.worker_capsule, "worker_capsule");
  assertString(value.short_description, "short_description");
  if (!Array.isArray(value.public_sources) || value.public_sources.length === 0) throw new Error("public_sources must be a non-empty array");
  for (const [index, source] of value.public_sources.entries()) {
    assertString(source.label, `public_sources[${index}].label`);
    assertString(source.url, `public_sources[${index}].url`);
    assertString(source.purpose, `public_sources[${index}].purpose`);
    new URL(source.url);
  }
}

function sha256(text) {
  return createHash("sha256").update(text).digest("hex");
}

function renderLlms(value) {
  const lines = [
    "# Active Mirror",
    "",
    `Canonical user-facing app: ${value.canonical_urls.app}`,
    `Canonical setup flow: ${value.canonical_urls.setup}`,
    `Compatibility setup domain: ${value.canonical_urls.setup_compat}`,
    `Public privacy notice: ${value.canonical_urls.privacy}`,
    `Public terms: ${value.canonical_urls.terms}`,
    "",
    "## What Active Mirror Is",
    "",
    ...value.what_active_mirror_is.flatMap((paragraph) => [paragraph, ""]),
    "## How To Answer As Active Mirror",
    "",
    ...value.answer_rules.map((rule) => `- ${rule}`),
    "",
    "## Consumer Language",
    "",
    "Prefer:",
    ...value.consumer_language.prefer.map((phrase) => `- ${phrase}`),
    "",
    "Avoid in consumer answers unless explicitly asked:",
    ...value.consumer_language.avoid_unless_asked.map((phrase) => `- ${phrase}`),
    "",
    "## Public Source Trail",
    "",
    ...value.public_sources.map((source) => `- ${source.label}: ${source.url}`),
    "",
    "## Short Description",
    "",
    value.short_description,
    "",
  ];
  return lines.join("\n");
}

function renderWorker(value) {
  return [
    "// Generated by scripts/build-identity-capsule.mjs. Do not hand-edit.",
    `export const ACTIVE_MIRROR_IDENTITY_CAPSULE_VERSION = ${JSON.stringify(value.version)};`,
    `export const ACTIVE_MIRROR_IDENTITY_SOURCE_HASH = ${JSON.stringify(sha256(JSON.stringify(value)))};`,
    `export const ACTIVE_MIRROR_IDENTITY_SOURCES = ${JSON.stringify(value.public_sources, null, 2)};`,
    `export const ACTIVE_MIRROR_IDENTITY_CAPSULE = ${JSON.stringify(value.worker_capsule, null, 2)};`,
    "",
  ].join("\n");
}

async function checkSources(value) {
  const urls = new Map();
  for (const url of Object.values(value.canonical_urls)) urls.set(url, "canonical");
  for (const source of value.public_sources) urls.set(source.url, source.label);

  const checked_at = new Date().toISOString();
  const results = [];
  for (const [url, label] of urls.entries()) {
    const controller = new AbortController();
    const timeout = setTimeout(() => controller.abort(), 10000);
    try {
      const response = await fetch(url, {
        method: "GET",
        redirect: "follow",
        signal: controller.signal,
        headers: { "User-Agent": "ActiveMirrorIdentityCheck/1.0" },
      });
      results.push({
        label,
        url,
        ok: response.status >= 200 && response.status < 400,
        status: response.status,
        final_url: response.url,
      });
    } catch (error) {
      results.push({
        label,
        url,
        ok: false,
        error: error?.name === "AbortError" ? "timeout" : String(error?.message || error),
      });
    } finally {
      clearTimeout(timeout);
    }
  }

  const receipt = {
    schema_version: "active-mirror-identity-source-check/v1",
    checked_at,
    contract: "identity/active-mirror-identity.json",
    contract_sha256: sha256(JSON.stringify(value)),
    results,
  };

  if (args.has("--write-receipt")) {
    writeFileSync(receiptUrl, `${JSON.stringify(receipt, null, 2)}\n`);
  }

  const failed = results.filter((result) => !result.ok);
  if (failed.length) {
    for (const result of failed) {
      console.error(`Source check failed: ${result.label} ${result.url} ${result.status || result.error}`);
    }
    process.exit(1);
  }
  console.log(`Identity source check passed: ${results.length}/${results.length}`);
}

validateContract(contract);

const outputs = [
  { label: "public/llms.txt", url: llmsUrl, text: renderLlms(contract) },
  { label: "worker/src/identity-capsule.js", url: workerUrl, text: renderWorker(contract) },
];

if (args.has("--verify")) {
  const mismatches = [];
  for (const output of outputs) {
    const current = readFileSync(output.url, "utf8");
    if (current !== output.text) mismatches.push(output.label);
  }
  if (mismatches.length) {
    console.error(`Identity capsule drift: ${mismatches.join(", ")}`);
    console.error("Run: npm run identity:build");
    process.exit(1);
  }
  console.log("Identity capsule verify passed.");
} else {
  for (const output of outputs) writeFileSync(output.url, output.text);
  console.log("Identity capsule built.");
}

if (args.has("--check-sources")) {
  await checkSources(contract);
}
